/**
 * Used to setup stats for a CloseCombatWeapon weapon. 
 *
 */
public class CloseCombatWeapon extends Weapon {
    CloseCombatWeapon(){
    this.AP=0;
    this.Range=0;
    this.S=0;
    this.Shots=0;
    this.isAssault=false;
    this.isHeavy=false;
    this.isMelee=true;
    this.isMelta=false;
    this.isRapidFire=false;
    this.name="CloseCombatWeapon";
    
    }
}
